package com.aca.guitarshop.model;

public class Strings {

	private String size;
	private StringBrand stringBrand;
	private String tone;
	
	
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public StringBrand getStringBrand() {
		return stringBrand;
	}
	public void setStringBrand(StringBrand stringBrand) {
		this.stringBrand = stringBrand;
	}
	public String getTone() {
		return tone;
	}
	public void setTone(String tone) {
		this.tone = tone;
	}
	
}
